    def start_simulation(self):
        """Start simulation with selected packet range and settings."""
        if not PYQTGRAPH_AVAILABLE:
            QMessageBox.warning(self, "Simulation Unavailable", 
                              "pyqtgraph is not available. Simulation cannot run.")
            return
        
        if self.total_packets == 0:
            QMessageBox.warning(self, "No File Loaded", 
                              "Please load a frequency binary file before starting simulation.")
            return
        
        # Get simulation parameters
        start_packet = self.sim_start_packet_spin.value()
        end_packet = self.sim_end_packet_spin.value()
        datarate = self.sim_datarate_spin.value()
        loop = self.sim_loop_checkbox.isChecked()
        
        # Validate parameters
        if start_packet < 0 or start_packet >= self.total_packets:
            QMessageBox.warning(self, "Invalid Start Packet", 
                              f"Start packet must be between 0 and {self.total_packets - 1}.")
            return
        
        if end_packet < 0 or end_packet >= self.total_packets:
            QMessageBox.warning(self, "Invalid End Packet", 
                              f"End packet must be between 0 and {self.total_packets - 1}.")
            return
        
        if start_packet > end_packet:
            QMessageBox.warning(self, "Invalid Range", 
                              "Start packet must be less than or equal to end packet.")
            return
        
        if datarate <= 0:
            QMessageBox.warning(self, "Invalid Datarate", 
                              "Datarate must be greater than 0.")
            return
        
        # Extract selected packet range
        selected_packets = self.packets[start_packet:end_packet + 1]
        
        if len(selected_packets) == 0:
            QMessageBox.warning(self, "No Packets Selected", 
                              "No packets in selected range.")
            return
        
        # Create or reuse simulation window
        if self.simulation_window is None or not self.simulation_window.isVisible():
            self.simulation_window = SimulationWindow(self)
        
        # Create simulated packet data objects
        simulated_packets = []
        for freq_packet in selected_packets:
            # Timestamp will be set when packet is fed
            simulated_packet = SimulatedPacketData(freq_packet, 0.0)
            simulated_packets.append(simulated_packet)
        
        # Start simulation
        self.simulation_window.start_simulation(simulated_packets, datarate, loop)
        self.simulation_window.show()
        self.simulation_window.raise_()
        self.simulation_window.activateWindow()
    
    def save_segment(self):
        """Save currently displayed packets to a new binary file."""
        if not self.packets or self.total_packets == 0:
            QMessageBox.warning(self, "No Data", "No packets loaded. Please open a file first.")
            return
        
        # Get currently displayed packets
        start_index = self.current_packet_index
        end_index = min(start_index + self.packets_per_plot, self.total_packets)
        packets_to_save = self.packets[start_index:end_index]
        
        if len(packets_to_save) == 0:
            QMessageBox.warning(self, "No Packets", "No packets to save.")
            return
        
        # Prompt for save location
        file_path, _ = QFileDialog.getSaveFileName(
            self,
            "Save Segment",
            "",
            "Binary Files (*.bin);;All Files (*)"
        )
        
        if not file_path:
            return  # User cancelled
        
        # Ensure .bin extension
        if not file_path.lower().endswith('.bin'):
            file_path += '.bin'
        
        try:
            # Write packets to file in big-endian I32 format
            with open(file_path, 'wb') as f:
                for packet in packets_to_save:
                    # Build array: [timestamp_delta, packet_number, ...frequency_i32_values]
                    array_length = 2 + len(packet.frequency_i32_values)
                    
                    if array_length == 2:
                        # Only timestamp delta and packet number (no frequency data)
                        packet_data = struct.pack('>2i', packet.timestamp_delta, packet.packet_number)
                    else:
                        # Pack all values together as big-endian I32
                        all_values = [packet.timestamp_delta, packet.packet_number] + packet.frequency_i32_values
                        packet_data = struct.pack(f'>{array_length}i', *all_values)
                    
                    f.write(packet_data)
            
            # Show success message
            num_packets = len(packets_to_save)
            QMessageBox.information(
                self,
                "Save Successful",
                f"Successfully saved {num_packets} packet(s) to:\n{file_path}"
            )
            self.statusBar().showMessage(f"Saved {num_packets} packet(s) to {file_path}")
            
        except Exception as e:
            QMessageBox.critical(
                self,
                "Save Error",
                f"Error saving segment to file:\n{e}"
            )
            import traceback
            traceback.print_exc()
            self.statusBar().showMessage(f"Error saving segment: {e}")
    
    def _on_analysis_button_clicked(self):
        """Handle click on peak analysis button."""
