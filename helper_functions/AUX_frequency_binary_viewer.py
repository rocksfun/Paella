    def _on_max_data_points_changed(self, value):
        """Handle change in max data points setting."""
        self.max_data_points = value
        # Trim data points if needed
        while len(self.data_points) > self.max_data_points:
            self.data_points.popleft()
    
    def _on_plot_style_changed(self, state):
        """Handle change in plot style (scatter vs lines)."""
        self.use_scatter = (state == Qt.CheckState.Checked.value)
        
        # Remove current plot item
        if self.plot_data_item == self.scatter_plot_item:
            self.plot_widget.removeItem(self.scatter_plot_item)
        else:
            self.plot_widget.removeItem(self.line_plot_item)
        
