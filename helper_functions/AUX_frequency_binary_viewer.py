@dataclass
class FrequencyPacket:
    """Represents a single packet from a frequency binary file."""
    timestamp_delta: int  # Original delta from file (scaled by 2^16)
    packet_number: int
    frequency_i32_values: List[int]  # Raw I32 values
    frequencies: List[float]  # Converted to Hz
    relative_timestamp: float  # Calculated relative time (seconds from file start)


class FrequencyPacketList:
    """A list-like wrapper for numpy arrays representing frequency packets."""
    def __init__(self, timestamp_deltas: np.ndarray, packet_numbers: np.ndarray, 
                 frequencies_i32: np.ndarray, frequencies_hz: np.ndarray, 
                 relative_timestamps: np.ndarray):
        self.timestamp_deltas = timestamp_deltas
        self.packet_numbers = packet_numbers
        self.frequencies_i32 = frequencies_i32
        self.frequencies_hz = frequencies_hz
        self.relative_timestamps = relative_timestamps
        self._length = len(timestamp_deltas)

    def __len__(self):
        return self._length

    def __getitem__(self, idx):
        if isinstance(idx, slice):
            return FrequencyPacketList(
                self.timestamp_deltas[idx],
                self.packet_numbers[idx],
                self.frequencies_i32[idx],
                self.frequencies_hz[idx],
                self.relative_timestamps[idx]
            )
        if idx < 0:
            idx += self._length
        if idx < 0 or idx >= self._length:
            raise IndexError("list index out of range")
        
        return FrequencyPacket(
            timestamp_delta=int(self.timestamp_deltas[idx]),
            packet_number=int(self.packet_numbers[idx]),
            frequency_i32_values=self.frequencies_i32[idx].tolist(),
            frequencies=self.frequencies_hz[idx].tolist(),
            relative_timestamp=float(self.relative_timestamps[idx])
        )
        
    def get_flattened_arrays(self, time_step: float) -> Tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray]:
        """Returns flattened 1D arrays of (times, frequencies, packet_numbers, relative_timestamps)
        Useful for fully vectorized processing."""
        N, M = self.frequencies_hz.shape
        time_offsets = -np.arange(M - 1, -1, -1) * time_step
        times_2d = self.relative_timestamps[:, np.newaxis] + time_offsets
        
        times = times_2d.flatten()
        freqs = self.frequencies_hz.flatten()
        pkt_nums = np.repeat(self.packet_numbers, M)
        return times, freqs, pkt_nums, times.copy()


def read_frequency_binary_file(file_path: str) -> List[FrequencyPacket]:
    """
    Read a frequency binary file and parse all packets.
    
    File format: Big-endian I32 arrays
    Structure per packet: [timestamp_delta, packet_number, ...frequency_i32_values]
    
    Args:
        file_path: Path to the binary file
        
    Returns:
        List of FrequencyPacket objects
        
    Raises:
        FileNotFoundError: If file doesn't exist
        ValueError: If file format is invalid
    """
    packets = []
    
    try:
        with open(file_path, 'rb') as f:
            file_data = f.read()
    except FileNotFoundError:
        raise FileNotFoundError(f"Frequency binary file not found: {file_path}")
    except Exception as e:
        raise ValueError(f"Error reading frequency binary file: {e}")
    
    if len(file_data) < 8:
        raise ValueError("Frequency binary file is too small (need at least 8 bytes for timestamp_delta and packet_number)")
    
    # Calculate how many I32 values we have total
    total_i32_count = len(file_data) // 4
    
    if total_i32_count < 2:
        raise ValueError("Frequency binary file contains less than 2 I32 values (need at least timestamp_delta and packet_number)")
    
    # Try to determine packet size by looking for consistent patterns
    # Common packet sizes: 64, 128, 256, 512 frequencies per packet
    # Packet size = 2 (header) + N (frequencies)
    assumed_frequencies_per_packet = 128
    assumed_packet_size_i32 = 2 + assumed_frequencies_per_packet  # 130 I32 values
    
    # Verify assumption by checking if file size is consistent
    num_complete_packets = total_i32_count // assumed_packet_size_i32
    remainder = total_i32_count % assumed_packet_size_i32
    
    # If remainder is not 0, try to find actual packet size
    if remainder != 0:
        # Try different packet sizes (common values: 64, 128, 256, 512 frequencies)
        for test_freq_count in [64, 128, 256, 512]:
            test_packet_size = 2 + test_freq_count
            test_num_packets = total_i32_count // test_packet_size
            test_remainder = total_i32_count % test_packet_size
            if test_remainder == 0 and test_num_packets > 0:
                assumed_frequencies_per_packet = test_freq_count
                assumed_packet_size_i32 = test_packet_size
                num_complete_packets = test_num_packets
                remainder = 0
                break
    
    if remainder != 0:
        # If we still have remainder, try to parse with variable packet sizes
        # This is more complex - for now, we'll parse what we can
        # and warn about incomplete packets
        print(f"Warning: File size suggests incomplete packets. "
              f"Total I32 values: {total_i32_count}, "
              f"Assumed packet size: {assumed_packet_size_i32}, "
              f"Remainder: {remainder}")
        # Continue with parsing, but may have incomplete last packet
    
    # Parse all packets
    scaling_factor = 12.5e6 / (2**32)  # Convert I32 to Hz
    
    # Convert entire file to numpy array for faster processing
    # Note: file_data is big-endian, but numpy defaults to native byte order
    # We need to handle big-endian conversion
    file_array = np.frombuffer(file_data, dtype='>i4')  # '>i4' = big-endian int32
    
    if remainder == 0 and num_complete_packets * assumed_packet_size_i32 == len(file_array):
        reshaped = file_array.reshape((num_complete_packets, assumed_packet_size_i32))
        timestamp_deltas = reshaped[:, 0]
        packet_numbers = reshaped[:, 1]
        frequencies_i32 = reshaped[:, 2:]
        frequencies_hz = frequencies_i32.astype(np.float64) * scaling_factor
        relative_timestamps = timestamp_deltas / 65536.0
        
        return FrequencyPacketList(
            timestamp_deltas, packet_numbers, frequencies_i32, frequencies_hz, relative_timestamps
        )
    else:
        for packet_idx in range(num_complete_packets):
            offset_i32 = packet_idx * assumed_packet_size_i32
            
            if offset_i32 + 2 > len(file_array):
                break  # Not enough data for header
            
            # Read timestamp delta and packet number (first 2 I32 values)
            timestamp_delta = int(file_array[offset_i32])
            packet_number = int(file_array[offset_i32 + 1])
            
            # Read all frequency I32 values at once using numpy slicing
            freq_start_i32 = offset_i32 + 2
            freq_end_i32 = freq_start_i32 + assumed_frequencies_per_packet
            
            if freq_end_i32 > len(file_array):
                break  # Not enough data
            
            # Extract frequency I32 values as numpy array
            frequency_i32_array = file_array[freq_start_i32:freq_end_i32]
            
            # Convert timestamp delta back to seconds
            # IMPORTANT: The timestamp_delta stored in the file is the ABSOLUTE time since
            # experiment start (packet.timestamp - experiment_start_time), NOT a delta between
            # consecutive packets. It was scaled by 2^16 when saved.
            absolute_time_seconds = timestamp_delta / 65536.0
            
            # Convert frequency I32 values to Hz using vectorized operation
            frequencies_array = frequency_i32_array.astype(np.float64) * scaling_factor
            
            # Convert to Python lists (if needed for FrequencyPacket)
            frequency_i32_values = frequency_i32_array.tolist()
            frequencies = frequencies_array.tolist()
            
            # Create packet
            packet = FrequencyPacket(
                timestamp_delta=timestamp_delta,
                packet_number=packet_number,
                frequency_i32_values=frequency_i32_values,
                frequencies=frequencies,
                relative_timestamp=absolute_time_seconds  # Use absolute time directly, not accumulated
            )
            
            packets.append(packet)
        
        if len(packets) == 0:
            raise ValueError("No valid packets found in frequency binary file")
        
        return packets


