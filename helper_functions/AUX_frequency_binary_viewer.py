    def _get_displayed_data(self) -> Optional[List[Tuple[float, float]]]:
        """Extract time/frequency arrays from currently displayed packets.
        
        Returns:
            List of (time, frequency) tuples, or None if no data available
        """
        # Check if we have packets loaded
        if self.total_packets == 0:
            return None
        
        if not self.packets or len(self.packets) == 0:
            return None
        
        if self.current_packet_index < 0 or self.current_packet_index >= self.total_packets:
            return None
        
        # Get range of packets to display
        end_index = min(self.current_packet_index + self.packets_per_plot, self.total_packets)
        packets_to_display = self.packets[self.current_packet_index:end_index]
        
        if not packets_to_display:
            return None
        
        # Get effective data rate
        data_rate = self.get_effective_data_rate()
        if data_rate <= 0:
            data_rate = 20000.0  # Default fallback
        time_step = 1.0 / data_rate
        
        # Extract time and frequency arrays
        time_freq_list = []
        
        if self.data_rate_override_enabled:
            # Recalculate times from scratch based on data_rate
            current_time = 0.0
            for packet in packets_to_display:
                if not packet.frequencies:
                    continue
                for freq in packet.frequencies:
                    time_freq_list.append((current_time, freq))
                    current_time += time_step
        else:
            # Use original packet timestamps
            for packet in packets_to_display:
                if not packet.frequencies:
                    continue
                num_frequencies = len(packet.frequencies)
                for i in range(num_frequencies):
                    frequency_time = packet.relative_timestamp - ((num_frequencies - 1 - i) * time_step)
                    time_freq_list.append((frequency_time, packet.frequencies[i]))
        
        return time_freq_list if time_freq_list else None


