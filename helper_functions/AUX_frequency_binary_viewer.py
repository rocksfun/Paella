        self.plot_widget.setLabel('left', 'Frequency', units='Hz')
        self.plot_widget.setLabel('bottom', 'Time', units='s')
        self.plot_widget.setTitle('Frequency vs Time')
        self.plot_widget.setBackground('#202124')  # Charcoal dark gray background
