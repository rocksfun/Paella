    def reprocess_file(self):
        """Reprocess the entire file with real-time analyzer and save to CSV."""
        if not self.packets or not self.current_file_path:
            return
        
        dir_name = os.path.dirname(self.current_file_path)
        base_name = os.path.basename(self.current_file_path)
        # Safe prefix extraction handling '_frequencies.bin' or '_packets.bin' or just '.bin'
        # The true prefix is always [delim1]_[delim2] (e.g. system_timestamp)
        parts = base_name.split('_')
        if len(parts) >= 2:
            prefix = f"{parts[0]}_{parts[1]}"
        else:
            prefix = os.path.splitext(base_name)[0]
        
        flags_file = os.path.join(dir_name, f"{prefix}_experiment_flags.txt")
        flags = []
        if os.path.exists(flags_file):
            with open(flags_file, 'r') as f:
                for line in f:
                    parts = line.strip().split('\t')  # TSV format
                    if len(parts) >= 2:
                        try:
                            flags.append((float(parts[0]), parts[1].strip()))
                        except ValueError:
                            pass
        flags.sort(key=lambda x: x[0])
        
        analyzer = RealTimeFrequencyAnalyzer(data_rate=self.data_rate, offline_mode=True)
        analyzer.settings = PeakDetectionSettings()
        
        self.statusBar().showMessage("Reprocessing file...")
        QApplication.processEvents()
        
        time_step = 1.0 / self.data_rate if self.data_rate > 0 else 5e-5
        
        if hasattr(self.packets, 'get_flattened_arrays'):
            # FAST PATH: Vectorized Processing
            times, freqs, pkt_nums, rel_times = self.packets.get_flattened_arrays(time_step)
            
            window_size_pts = int(self.data_rate * 10.0)
            step_size_pts = int(self.data_rate * 9.5)
            if window_size_pts <= 0: window_size_pts = 200000
            if step_size_pts <= 0: step_size_pts = 190000
            
            total_points = len(times)
            for start_idx in range(0, max(1, total_points), step_size_pts):
                end_idx = min(start_idx + window_size_pts, total_points)
                
                if end_idx > start_idx:
                    analyzer.latest_buffer_time = float(times[end_idx-1])
                    analyzer.process_arrays(
                        times[start_idx:end_idx],
                        freqs[start_idx:end_idx],
                        pkt_nums[start_idx:end_idx],
                        rel_times[start_idx:end_idx]
                    )
                
                if start_idx % (step_size_pts * 20) == 0:
                    self.statusBar().showMessage(f"Reprocessing {(end_idx/total_points)*100:.1f}%...")
                    QApplication.processEvents()
        else:
            # SLOW PATH: Fallback for truncated/corrupted files
            points_added = 0
            points_between_process = int(0.5 * self.data_rate) if self.data_rate > 0 else 10000
            
            for p_idx, packet in enumerate(self.packets):
                num_freqs = len(packet.frequencies)
                if num_freqs == 0:
                    continue
                
                recv_timestamp = packet.relative_timestamp
                pkt_num = packet.packet_number
                
                # Vectorized tuple creation
                data_points = [
                    (recv_timestamp - ((num_freqs - 1 - i) * time_step), freq, pkt_num, recv_timestamp - ((num_freqs - 1 - i) * time_step))
                    for i, freq in enumerate(packet.frequencies)
                ]
                    
                analyzer.add_data_points(data_points)
                points_added += num_freqs
                
                if points_added >= points_between_process:
                    analyzer.process()
                    points_added = 0
                    
                if p_idx % 10000 == 0:
                    self.statusBar().showMessage(f"Reprocessing packet {p_idx}/{self.total_packets}...")
                    QApplication.processEvents()
            
            analyzer.process()
        
        import csv
        csv_filename = os.path.join(dir_name, f"{prefix}_uncalibrated_peaks.csv")
        headers = [
            'condition', 'peak_time', 'approximate_mass_pg', 'peak_width_ms', 
            'baseline_hz', 'peak1_delta_hz', 'peak2_delta_hz', 'height_diff_percent',
            'packet_number', 'relative_time', 'node_dev', 'antinode_difference_raw',
            'mass_raw1', 'mass_raw2', 'baseline_noise', 'peak_noise', 'baseline_slope'
        ]
        
        try:
            with open(csv_filename, 'w', newline='') as csvfile:
                writer = csv.DictWriter(csvfile, fieldnames=headers)
                writer.writeheader()
                matched_pairs_for_csv = analyzer.get_matched_pairs_for_csv()
                for pair in matched_pairs_for_csv:
                    # Time elapsed since streaming started
                    pair_time = pair.get('peak1_time', 0.0)
                    
                    current_condition = "Unknown"
                    if len(flags) > 0:
                        current_condition = flags[0][1]
                        for f_time, f_cond in flags:
                            if pair_time >= f_time:
                                current_condition = f_cond
                            else:
                                break
                    avg_peak_time = (pair['peak1_time'] + pair['peak2_time']) / 2.0
                    peak1_freq = pair['peak1_frequency']
                    peak2_freq = pair['peak2_frequency']
                    peak1_baseline = pair['peak1_baseline']
                    peak2_baseline = pair['peak2_baseline']
                    avg_baseline = (peak1_baseline + peak2_baseline) / 2.0
                    
                    row_data = {
                        'condition': current_condition,
                        'peak_time': f"{avg_peak_time:.6f}",
                        'approximate_mass_pg': f"{pair.get('mass_raw', 0.0):.6f}",
                        'peak_width_ms': f"{pair.get('separation_time', 0) * 1000.0:.6f}",
                        'baseline_hz': f"{avg_baseline:.6f}",
                        'peak1_delta_hz': f"{peak1_baseline - peak1_freq:.6f}",
                        'peak2_delta_hz': f"{peak2_baseline - peak2_freq:.6f}",
                        'height_diff_percent': f"{pair.get('height_diff_percent', 0.0):.6f}",
                        'packet_number': pair.get('packet_number', 0),
                        'relative_time': f"{pair.get('relative_time', 0.0):.6f}",
                        'node_dev': f"{pair.get('node_dev', 0.0):.6f}",
                        'antinode_difference_raw': f"{pair.get('antinode_difference_raw', 0.0):.6f}",
                        'mass_raw1': f"{pair.get('mass_raw1', 0.0):.6f}",
                        'mass_raw2': f"{pair.get('mass_raw2', 0.0):.6f}",
                        'baseline_noise': f"{pair.get('baseline_noise', 0.0):.6f}",
                        'peak_noise': f"{pair.get('peak_noise', 0.0):.6f}",
                        'baseline_slope': f"{pair.get('baseline_slope', 0.0):.6f}"
                    }
                    writer.writerow(row_data)
            self.statusBar().showMessage("Ready")
            QMessageBox.information(self, "Success", f"Reprocessed file into {os.path.basename(csv_filename)}")
        except Exception as e:
            QMessageBox.critical(self, "Error", f"Failed to save {os.path.basename(csv_filename)}:\n{e}")
            self.statusBar().showMessage("Ready")
            
    def _calculate_statistics(self):
        """Calculate and display file statistics."""
        if not self.packets:
            self.total_packets_stat_label.setText("--")
            self.file_duration_label.setText("--")
            self.data_rate_label.setText("--")
            self.freq_range_label.setText("--")
            return
        
        # Total packets
        self.total_packets_stat_label.setText(str(self.total_packets))
        
        # File duration (time from first to last packet)
        if self.total_packets > 0:
            first_time = self.packets[0].relative_timestamp
            last_time = self.packets[-1].relative_timestamp
            duration = last_time - first_time
            self.file_duration_label.setText(f"{duration:.3f} s")
            
            # Estimate data rate from packet timestamps
            if self.total_packets > 1:
                # Average time between packets
                time_diffs = []
                for i in range(1, self.total_packets):
                    time_diff = self.packets[i].relative_timestamp - self.packets[i-1].relative_timestamp
                    if time_diff > 0:
                        time_diffs.append(time_diff)
                
                if time_diffs:
                    avg_time_between_packets = np.mean(time_diffs)
                    # If each packet has N frequencies, and they're measured sequentially,
                    # the time per packet should be approximately N / data_rate
                    # So data_rate ≈ N / avg_time_between_packets
                    if len(self.packets[0].frequencies) > 0:
                        num_freqs = len(self.packets[0].frequencies)
                        estimated_rate = num_freqs / avg_time_between_packets
                        self.data_rate = estimated_rate
                        self.data_rate_label.setText(f"{estimated_rate:.1f} Hz")
                    else:
                        self.data_rate_label.setText("--")
                else:
                    self.data_rate_label.setText("--")
            else:
                self.data_rate_label.setText("--")
        else:
            self.file_duration_label.setText("--")
            self.data_rate_label.setText("--")
        
        # Frequency range (across all packets)
        all_frequencies = []
        for packet in self.packets:
            all_frequencies.extend(packet.frequencies)
        
        if all_frequencies:
            freq_min = min(all_frequencies)
            freq_max = max(all_frequencies)
            self.freq_range_label.setText(f"{freq_min:.3f} - {freq_max:.3f} Hz")
        else:
            self.freq_range_label.setText("--")
    
