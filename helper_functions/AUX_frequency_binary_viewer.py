    def run_analysis(self):
        """Estimate and display baseline for current data."""
        if not FREQUENCY_ANALYSIS_AVAILABLE:
            QMessageBox.warning(self, "Analysis Unavailable", 
                              "Frequency analysis module is not available.")
            return
        
        if not self.current_data:
            QMessageBox.warning(self, "No Data", 
                              "No data available. Please ensure data is loaded in the main window.")
            return
        
        if len(self.current_data) < 10:
            QMessageBox.warning(self, "Insufficient Data", 
                              f"Not enough data points for baseline estimation. Got {len(self.current_data)} points, need at least 10 points.\n\n"
                              f"Try increasing 'Packets per plot' in the main window.")
            return
        
        try:
            # Extract time and frequency arrays
            times = np.array([d[0] for d in self.current_data], dtype=np.float64)
            frequencies = np.array([d[1] for d in self.current_data], dtype=np.float64)
            
            # Verify data transfer
            self.statusBar().showMessage(f"Estimating baseline for {len(frequencies)} data points...")
            
            # Apply smoothing filter to frequency data
            # Use Savitzky-Golay filter for smoothing
            try:
                from scipy import signal
                # Get filter window size from control (must be odd)
                filter_window_samples = self.filter_width_spin.value()
                if filter_window_samples % 2 == 0:
                    filter_window_samples += 1  # Must be odd
                filter_window_samples = min(filter_window_samples, len(frequencies) - (len(frequencies) % 2))
                
                if filter_window_samples >= 5 and len(frequencies) >= filter_window_samples:
                    self.filtered_frequencies = signal.savgol_filter(
                        frequencies,
                        window_length=filter_window_samples,
                        polyorder=2  # 2nd order polynomial
                    )
                else:
                    # Not enough data for filtering, use raw data
                    self.filtered_frequencies = frequencies.copy()
            except ImportError:
                # scipy not available, use raw data
                warnings.warn("scipy not available, using unfiltered data")
                self.filtered_frequencies = frequencies.copy()
            except Exception as e:
                warnings.warn(f"Filtering failed: {e}, using unfiltered data")
                self.filtered_frequencies = frequencies.copy()
            
            # Estimate baseline using rolling median on filtered data
            from helper_functions.SMR_frequency_processing_functions import estimate_baseline_rolling_median, FilterSettings
            
            filter_settings = FilterSettings()
            baseline_estimate = estimate_baseline_rolling_median(
                self.filtered_frequencies,
                self.data_rate,
                window_size_seconds=filter_settings.quantile_width  # Use 0.25s default window
            )
            
            # Estimate sensitivity from baseline frequency (carrier frequency)
            # Using R formula: minSensitivityEstimate = 0.8794*(min(carriers)/1e6)^1.5+0.0367
            baseline_mean = np.mean(baseline_estimate)
            if baseline_mean > 0:
                # Estimate sensitivity in Hz/pg
                min_sensitivity_estimate = 0.8794 * (baseline_mean / 1e6) ** 1.5 + 0.0367
                self.sensitivity_hz_per_pg = min_sensitivity_estimate
            else:
                # Fallback to default
                self.sensitivity_hz_per_pg = 1.0
            
            # Create a minimal DataStructure for visualization
            from helper_functions.SMR_frequency_processing_functions import DataStructure
            
            dat = np.column_stack([times, frequencies])
            dat_hp = np.column_stack([times, baseline_estimate])
            
            self.current_data_structure = DataStructure(
                dat=dat,
                dat_bp=None,
                dat_lp=np.column_stack([times, self.filtered_frequencies]) if self.filtered_frequencies is not None else None,
                dat_hp=dat_hp,
                timestamps=times,
                frequencies=frequencies,
                data_rate=self.data_rate,
                sample_info={},
                baseline_mean=np.mean(baseline_estimate),
                baseline_std=np.std(baseline_estimate),
                baseline_regions=[],
                chunks=[]
            )
            
            self.current_peaks = None  # No peak detection yet
            
            # Update display
            self.update_plot()
            
            # Show baseline statistics
            baseline_mean = np.mean(baseline_estimate)
            baseline_std = np.std(baseline_estimate)
            baseline_range = np.max(baseline_estimate) - np.min(baseline_estimate)
            
            self.statusBar().showMessage(
                f"Baseline estimated: mean={baseline_mean:.4f} Hz, "
                f"std={baseline_std:.4f} Hz, range={baseline_range:.4f} Hz"
            )
            
            # Detect individual peaks (regions below threshold)
            self._detect_individual_peaks()
            
            # Match peaks into pairs
            self._match_peak_pairs()
            
            # Update plot and table with detected peaks
            self.update_plot()
            self.update_table()
            
            # Update statistics panel
            self.update_statistics()
            
        except Exception as e:
            QMessageBox.critical(self, "Baseline Estimation Error", f"Error during baseline estimation:\n{e}")
            import traceback
            traceback.print_exc()
    
    def _detect_individual_peaks(self):
        """Detect individual peaks by finding regions below threshold and their local minima."""
        try:
            if not self.current_data_structure or not self.current_data:
                self.detected_individual_peaks = []
                return
            
            times = self.current_data_structure.timestamps
            
            # Use filtered frequencies for detection (if available), otherwise use raw frequencies
            if self.filtered_frequencies is not None and len(self.filtered_frequencies) == len(times):
                frequencies_for_detection = self.filtered_frequencies
            else:
                frequencies_for_detection = self.current_data_structure.frequencies
            
            if len(times) == 0 or len(frequencies_for_detection) == 0:
                self.detected_individual_peaks = []
                return
            
            # Get threshold
            threshold_value = self.detection_threshold_spin.value()
            
            # Calculate threshold line values (baseline + threshold)
            if self.current_data_structure.dat_hp is not None:
                baseline_frequencies = self.current_data_structure.dat_hp[:, 1]
                if len(baseline_frequencies) != len(times):
                    self.detected_individual_peaks = []
                    return
                threshold_frequencies = baseline_frequencies + threshold_value
            elif self.current_data_structure.baseline_mean is not None:
                threshold_frequencies = np.full(len(times), self.current_data_structure.baseline_mean + threshold_value)
            else:
                self.detected_individual_peaks = []
                return
            
            # Ensure arrays have matching lengths
            if len(threshold_frequencies) != len(frequencies_for_detection):
                self.detected_individual_peaks = []
                return
            
            # Find regions where filtered frequency < threshold
            below_threshold = frequencies_for_detection < threshold_frequencies
            
            if not np.any(below_threshold):
                self.detected_individual_peaks = []
                return
            
            # Find contiguous regions below threshold
            peak_regions = []
            in_region = False
            region_start = None
            
            for i, is_below in enumerate(below_threshold):
                if is_below and not in_region:
                    region_start = i
                    in_region = True
                elif not is_below and in_region:
                    if region_start is not None:
                        peak_regions.append((region_start, i - 1))
                    in_region = False
                    region_start = None
            
            # Handle case where data ends in a region
            if in_region and region_start is not None:
                peak_regions.append((region_start, len(below_threshold) - 1))
            
            # Find local minimum (deepest point) in each region
            detected_peaks = []
            for start_idx, end_idx in peak_regions:
                if end_idx >= start_idx:
                    # Use filtered frequencies for finding minimum
                    region_frequencies = frequencies_for_detection[start_idx:end_idx + 1]
                    region_times = times[start_idx:end_idx + 1]
                    
                    # Find index of minimum frequency in this region (relative to region)
                    min_idx_rel = np.argmin(region_frequencies)
                    min_idx_abs = start_idx + min_idx_rel
                    
                    # Get values at minimum (use filtered frequency for detection, but show raw frequency in table)
                    peak_time = region_times[min_idx_rel]
                    peak_frequency_filtered = region_frequencies[min_idx_rel]
                    # Also get raw frequency at this location for display
                    peak_frequency_raw = self.current_data_structure.frequencies[min_idx_abs] if min_idx_abs < len(self.current_data_structure.frequencies) else peak_frequency_filtered
                    
                    # Calculate deviation from baseline (using filtered frequency)
                    if self.current_data_structure.dat_hp is not None:
                        baseline_at_peak = baseline_frequencies[min_idx_abs]
                    else:
                        baseline_at_peak = self.current_data_structure.baseline_mean
                    
                    deviation = peak_frequency_filtered - baseline_at_peak
                    
                    detected_peaks.append({
                        'index': min_idx_abs,
                        'time': peak_time,
                        'frequency': peak_frequency_raw,  # Show raw frequency in table
                        'frequency_filtered': peak_frequency_filtered,  # Filtered frequency used for detection
                        'deviation': deviation,  # Deviation of filtered frequency from baseline
                        'baseline': baseline_at_peak,
                        'region_start': start_idx,
                        'region_end': end_idx
                    })
            
            self.detected_individual_peaks = detected_peaks
            
        except Exception as e:
            # Don't crash on peak detection errors - just log and clear peaks
            import traceback
            print(f"Error in peak detection: {e}")
            traceback.print_exc()
            self.detected_individual_peaks = []
            self.statusBar().showMessage(f"Error detecting peaks: {e}")
    
    def _match_peak_pairs(self):
        """
        Match individual peaks into pairs using R algorithm (sequential matching).
        
        Matches consecutive peaks that meet all criteria:
        1. Time gap between peaks is within tDoubletGap range
        2. Absolute height difference is below threshold
        3. Next peak is further away (triplet prevention)
        """
        if len(self.detected_individual_peaks) < 2:
            self.matched_peak_pairs = []
            return
        
        # Get matching parameters (matching R defaults)
        t_doublet_gap_min = self.doublet_gap_min_spin.value()  # seconds
        t_doublet_gap_max = self.doublet_gap_max_spin.value()  # seconds
        max_height_diff = self.max_height_diff_spin.value()  # Absolute Hz difference
        
        # Convert time gaps to samples (like R does)
        gap_min_samples = t_doublet_gap_min * self.data_rate
        gap_max_samples = t_doublet_gap_max * self.data_rate
        
        # Sort peaks by time
        sorted_peaks = sorted(self.detected_individual_peaks, key=lambda p: p['time'])
        
        # Calculate time differences between consecutive peaks (in samples)
        time_between_samples = []
        for i in range(len(sorted_peaks) - 1):
            time_diff_seconds = sorted_peaks[i + 1]['time'] - sorted_peaks[i]['time']
            time_between_samples.append(time_diff_seconds * self.data_rate)
        
        matched_pairs = []
        
        # Sequential matching (like R): only check consecutive peaks (i, i+1)
        for i in range(len(sorted_peaks) - 1):
            peak1 = sorted_peaks[i]
            peak2 = sorted_peaks[i + 1]
            
            # Time to next peak (in samples)
            time_to_next = time_between_samples[i]
            
            # Time after next peak (for triplet prevention)
            time_after_next = time_between_samples[i + 1] if i + 1 < len(time_between_samples) else 1e6
            
            # Calculate absolute height difference
            height1 = abs(peak1['deviation'])
            height2 = abs(peak2['deviation'])
            height_diff = abs(height1 - height2)
            
            # R matching criteria (from getPeaks.R line 115):
            # 1. timeToNext > tDoubletGap[1] & timeToNext < tDoubletGap[2]
            # 2. abs(heights-nextHeight) < maxHeightDiff
            # 3. timeAfterNext > timeToNext (triplet prevention)
            if (time_to_next > gap_min_samples and 
                time_to_next < gap_max_samples and 
                height_diff < max_height_diff and  # Absolute difference, not percentage!
                time_after_next > time_to_next):
                
                # All criteria met - create matched pair
                separation_time = time_to_next / self.data_rate  # Convert back to seconds
                
                matched_pairs.append({
                    'peak1': peak1,
                    'peak2': peak2,
                    'peak1_time': peak1['time'],
                    'peak2_time': peak2['time'],
                    'peak1_deviation': peak1['deviation'],
                    'peak2_deviation': peak2['deviation'],
                    'peak1_depth': height1,
                    'peak2_depth': height2,
                    'separation_time': separation_time,
                    'mean_deviation': (height1 + height2) / 2.0,
                    'height_diff_percent': height_diff / max(height1, height2, 1e-10) * 100.0,
                    'height_diff_abs': height_diff
                })
        
        self.matched_peak_pairs = matched_pairs
        
        # Filter matched pairs based on percentage difference
        self._filter_accepted_pairs()
    
    def _filter_accepted_pairs(self):
        """Filter matched pairs based on maximum percentage difference."""
        max_percent_diff = self.max_percent_diff_spin.value()
        self.accepted_matched_pairs = [
            pair for pair in self.matched_peak_pairs
            if pair['height_diff_percent'] <= max_percent_diff
        ]
    
    def update_plot(self):
        """Update plot with baseline estimate."""
        if not self.analysis_plot or not self.current_data_structure:
            return
        
        times = self.current_data_structure.timestamps
        if len(times) == 0:
            return
        
        # Update filtered data overlay (lime green line) - smoothed frequency data
        if self.filtered_frequencies is not None and len(self.filtered_frequencies) == len(times):
            self.filtered_data_item.setData(times, self.filtered_frequencies)
        else:
            self.filtered_data_item.setData([], [])
        
        # Update baseline overlay (yellow line) - this is the estimated baseline
        # Using rolling median baseline estimation
        if self.current_data_structure.dat_hp is not None:
            # Use the baseline estimate from rolling median
            baseline_frequencies = self.current_data_structure.dat_hp[:, 1]
            self.baseline_plot_item.setData(times, baseline_frequencies)
        elif self.current_data_structure.baseline_mean is not None:
            # Fallback to constant baseline if baseline estimate not available
            baseline_array = np.full(len(times), self.current_data_structure.baseline_mean)
            self.baseline_plot_item.setData(times, baseline_array)
        
        # Update threshold line (magenta dashed line) - shows detection threshold
        # Peaks are detected when frequency goes below this threshold relative to baseline
        threshold = self.detection_threshold_spin.value()
        
        if self.current_data_structure.dat_hp is not None:
            # Use time-varying baseline estimate
            baseline_frequencies = self.current_data_structure.dat_hp[:, 1]
            # Threshold line = baseline + threshold (threshold is negative, e.g., -4 Hz)
            threshold_frequencies = baseline_frequencies + threshold
            self.threshold_line_item.setData(times, threshold_frequencies)
        elif self.current_data_structure.baseline_mean is not None:
            # Fallback to constant baseline
            threshold_absolute = self.current_data_structure.baseline_mean + threshold
            threshold_array = np.full(len(times), threshold_absolute)
            self.threshold_line_item.setData(times, threshold_array)
        else:
            self.threshold_line_item.setData([], [])
        
        # Highlight regions below threshold and mark peak minima
        self._update_peak_regions_visualization()
        
        # Update matched pair visualization
        self._update_pair_visualization()
        
        # Hide other overlays for now
        self.detection_data_item.setData([], [])
    
    def _update_peak_regions_visualization(self):
        """Update visualization of regions below threshold and peak markers."""
        if not self.analysis_plot or not self.current_data_structure:
            return
        
        times = self.current_data_structure.timestamps
        frequencies = self.current_data_structure.frequencies
        
        if len(times) == 0:
            return
        
        # Remove old region highlights
        for region_item in self.below_threshold_regions:
            self.analysis_plot.removeItem(region_item)
        self.below_threshold_regions.clear()
        
        # Clear peak markers
        self.peak_markers_item.setData([], [])
        
        # Clear peak number labels
        for label_item in self.peak_label_items:
            self.analysis_plot.removeItem(label_item)
        self.peak_label_items.clear()
        
        # Clear pair labels
        for label_item in self.pair_label_items:
            self.analysis_plot.removeItem(label_item)
        self.pair_label_items.clear()
        
        if not self.detected_individual_peaks:
            return
        
        # Get threshold frequencies for shading
        threshold_value = self.detection_threshold_spin.value()
        if self.current_data_structure.dat_hp is not None:
            baseline_frequencies = self.current_data_structure.dat_hp[:, 1]
            threshold_frequencies = baseline_frequencies + threshold_value
        elif self.current_data_structure.baseline_mean is not None:
            threshold_frequencies = np.full(len(times), self.current_data_structure.baseline_mean + threshold_value)
        else:
            return
        
        # Highlight each peak region with shaded area
        peak_times = []
        peak_freqs = []
        
        # Use filtered frequencies for visualization if available
        if self.filtered_frequencies is not None and len(self.filtered_frequencies) == len(times):
            frequencies_for_viz = self.filtered_frequencies
        else:
            frequencies_for_viz = frequencies
        
        for peak in self.detected_individual_peaks:
            start_idx = peak['region_start']
            end_idx = peak['region_end']
            
            # Validate indices
            if (start_idx >= 0 and end_idx >= start_idx and 
                start_idx < len(times) and end_idx < len(times) and
                start_idx < len(frequencies_for_viz) and end_idx < len(frequencies_for_viz) and
                start_idx < len(threshold_frequencies) and end_idx < len(threshold_frequencies)):
                # Add peak marker at local minimum (use filtered frequency for marker position)
                peak_times.append(peak['time'])
                # Use filtered frequency for marker position to match detection
                peak_freqs.append(peak.get('frequency_filtered', peak['frequency']))
        
        # Update peak markers
        if peak_times:
            self.peak_markers_item.setData(peak_times, peak_freqs)
            
            # Create a mapping from peak index to pair number (if matched - only accepted pairs)
            peak_to_pair = {}
            for pair_idx, pair in enumerate(self.accepted_matched_pairs):
                peak_to_pair[pair['peak1']['index']] = pair_idx + 1
                peak_to_pair[pair['peak2']['index']] = pair_idx + 1
            
            # Add peak number labels (raw numbers for unmatched, pair numbers for matched)
            for peak in self.detected_individual_peaks:
                peak_time = peak['time']
                peak_freq = peak.get('frequency_filtered', peak['frequency'])
                peak_index = peak['index']
                
                # Check if this peak is part of a matched pair
                if peak_index in peak_to_pair:
                    # Use pair label (p1, p2, etc.)
                    label_text = f"p{peak_to_pair[peak_index]}"
                else:
                    # Use raw peak number
                    label_text = str(peak_index + 1)  # 1-based numbering
                
                # Create text label for this peak
                label = pg.TextItem(
                    text=label_text,
                    color='#FFFFFF',
                    anchor=(0.5, 1.5),  # Anchor below the point
                    border=pg.mkPen('#000000', width=1),
                    fill=pg.mkBrush('#000000', alpha=128)
                )
                label.setPos(peak_time, peak_freq)
                self.analysis_plot.addItem(label)
                self.peak_label_items.append(label)
    
    def _update_pair_visualization(self):
        """Update visualization of matched peak pairs with connecting lines."""
        if not self.analysis_plot or not self.accepted_matched_pairs:
            self.pair_lines_item.setData([], [])
            return
        
        # Build arrays for connecting lines
        # For multiple disconnected segments, we need to use NaN to break lines
        line_times = []
        line_freqs = []
        
        poly1_t, poly1_f = [], []
        poly2_t, poly2_f = [], []
        
        node_t, node_f = [], []
        
        # We need the full frequency buffer and time array
        times = self.current_data_structure.timestamps
        if self.filtered_frequencies is not None and len(self.filtered_frequencies) == len(times):
            full_freqs = self.filtered_frequencies
        else:
            full_freqs = self.current_data_structure.frequencies
            
        for row, pair in enumerate(self.accepted_matched_pairs):
            peak1_time = pair['peak1_time']
            peak2_time = pair['peak2_time']
            peak1_freq = pair['peak1'].get('frequency_filtered', pair['peak1']['frequency'])
            peak2_freq = pair['peak2'].get('frequency_filtered', pair['peak2']['frequency'])
            
            # Add line segment: peak1 -> peak2
            if line_times:  # Add NaN to break from previous segment
                line_times.append(np.nan)
                line_freqs.append(np.nan)
                poly1_t.append(np.nan)
                poly1_f.append(np.nan)
                poly2_t.append(np.nan)
                poly2_f.append(np.nan)
            
            line_times.extend([peak1_time, peak2_time])
            line_freqs.extend([peak1_freq, peak2_freq])
            
            # Do local polynomial fits
            peak1_idx = pair['peak1']['index']
            peak2_idx = pair['peak2']['index']
            sep_pts = peak2_idx - peak1_idx
            tip_pts = max(5, int(0.15 * sep_pts)) if sep_pts > 0 else 5
            
            def fit_tip(p_idx):
                start_p = max(0, p_idx - tip_pts)
                end_p = min(len(full_freqs), p_idx + tip_pts + 1)
                y = full_freqs[start_p:end_p]
                x = np.arange(len(y))
                if len(y) < 5: return [], [], 0.0
                try:
                    import warnings
                    with warnings.catch_warnings():
                        warnings.simplefilter('ignore')
                        c = np.polyfit(x, y, 4)
                    return times[start_p:end_p], np.polyval(c, x), np.sqrt(np.mean((y - np.polyval(c, x))**2))
                except Exception:
                    return [], [], 0.0
                    
            t1, f1, rmse1 = fit_tip(peak1_idx)
            t2, f2, rmse2 = fit_tip(peak2_idx)
            
            poly1_t.extend(t1)
            poly1_f.extend(f1)
            poly2_t.extend(t2)
            poly2_f.extend(f2)
            
            # Find the node (maximum) between the two peaks
            node_search_idx = slice(peak1_idx, peak2_idx + 1)
            if peak2_idx > peak1_idx:
                node_search = full_freqs[node_search_idx]
                n_max = np.argmax(node_search)
                node_abs = peak1_idx + n_max
                node_t.append(times[node_abs])
                node_f.append(full_freqs[node_abs])
            
            # Calculate offline-equivalent mass features to inject quietly into the pair dict for the table
            baseline_val = pair['peak1']['baseline']
            node_dev_hz = np.max(full_freqs[node_search_idx]) - baseline_val if peak2_idx > peak1_idx else 0.0
            mass1_raw = baseline_val - full_freqs[peak1_idx]
            mass2_raw = baseline_val - full_freqs[peak2_idx]
            peak_noise = np.sqrt((rmse1**2 + rmse2**2) / 2.0)
            
            # Store temporarily for update_table to render
            pair['_tmp_node_dev'] = node_dev_hz
            pair['_tmp_mass_raw1'] = abs(mass1_raw) / max(1e-9, getattr(self, 'sensitivity_hz_per_pg', 1.0))
            pair['_tmp_mass_raw2'] = abs(mass2_raw) / max(1e-9, getattr(self, 'sensitivity_hz_per_pg', 1.0))
            pair['_tmp_tip_rmse'] = peak_noise
            
        if line_times:
            self.pair_lines_item.setData(np.array(line_times), np.array(line_freqs))
        else:
            self.pair_lines_item.setData([], [])
            
        if poly1_t: self.poly1_lines_item.setData(np.array(poly1_t), np.array(poly1_f))
        else: self.poly1_lines_item.setData([], [])
        
        if poly2_t: self.poly2_lines_item.setData(np.array(poly2_t), np.array(poly2_f))
        else: self.poly2_lines_item.setData([], [])
        
        if node_t: self.node_markers_item.setData(np.array(node_t), np.array(node_f))
        else: self.node_markers_item.setData([], [])
    
    def update_table(self):
        """Update results tables with matched peak pairs and unmatched peaks."""
        # Update matched pairs table (show only accepted pairs)
        if not self.accepted_matched_pairs:
            self.matched_table.setRowCount(0)
        else:
            self.matched_table.setRowCount(len(self.accepted_matched_pairs))
            for row, pair in enumerate(self.accepted_matched_pairs):
                self.matched_table.setItem(row, 0, QTableWidgetItem(str(row + 1)))
                
                # Convert deviations to display units
                peak1_dev_display = self._convert_to_display_units(abs(pair['peak1_deviation']))
                peak2_dev_display = self._convert_to_display_units(abs(pair['peak2_deviation']))
                mean_dev_display = self._convert_to_display_units(pair['mean_deviation'])
                
                self.matched_table.setItem(row, 1, QTableWidgetItem(f"{peak1_dev_display:.4f}"))
                self.matched_table.setItem(row, 2, QTableWidgetItem(f"{peak2_dev_display:.4f}"))
                self.matched_table.setItem(row, 3, QTableWidgetItem(f"{mean_dev_display:.4f}"))
                
                # Separation in milliseconds
                separation_ms = pair['separation_time'] * 1000.0
                self.matched_table.setItem(row, 4, QTableWidgetItem(f"{separation_ms:.3f}"))
                
                # Peak difference: "Hz (%)" format where Hz toggles between Hz and pg
                height_diff_abs = pair.get('height_diff_abs', abs(abs(pair['peak1_deviation']) - abs(pair['peak2_deviation'])))
                height_diff_display = self._convert_to_display_units(height_diff_abs)
                height_diff_percent = pair['height_diff_percent']
                self.matched_table.setItem(row, 5, QTableWidgetItem(f"{height_diff_display:.4f} ({height_diff_percent:.2f}%)"))
                
                # Write newly injected temp values
                self.matched_table.setItem(row, 6, QTableWidgetItem(f"{pair.get('_tmp_node_dev', 0.0):.4f}"))
                self.matched_table.setItem(row, 7, QTableWidgetItem(f"{pair.get('_tmp_mass_raw1', 0.0):.4f}"))
                self.matched_table.setItem(row, 8, QTableWidgetItem(f"{pair.get('_tmp_mass_raw2', 0.0):.4f}"))
                self.matched_table.setItem(row, 9, QTableWidgetItem(f"{pair.get('_tmp_tip_rmse', 0.0):.4f}"))
        
        # Update unmatched peaks table
        # Create mapping from peak index to pair number
        peak_to_pair = {}
        for pair_idx, pair in enumerate(self.matched_peak_pairs):
            peak_to_pair[pair['peak1']['index']] = pair_idx + 1
            peak_to_pair[pair['peak2']['index']] = pair_idx + 1
        
        # Show all peaks in unmatched table, but indicate which are matched
        if not self.detected_individual_peaks:
            self.unmatched_table.setRowCount(0)
        else:
            self.unmatched_table.setRowCount(len(self.detected_individual_peaks))
            for row, peak in enumerate(self.detected_individual_peaks):
                peak_index = peak['index']
                self.unmatched_table.setItem(row, 0, QTableWidgetItem(str(peak_index + 1)))
                
                # Show pair number if matched, otherwise empty
                if peak_index in peak_to_pair:
                    pair_num_str = f"p{peak_to_pair[peak_index]}"
                else:
                    pair_num_str = "--"
                self.unmatched_table.setItem(row, 1, QTableWidgetItem(pair_num_str))
                
                self.unmatched_table.setItem(row, 2, QTableWidgetItem(f"{peak['time']:.6f}"))
                self.unmatched_table.setItem(row, 3, QTableWidgetItem(f"{peak['frequency']:.4f}"))
                
                # Convert deviation and baseline to display units
                deviation_display = self._convert_to_display_units(abs(peak['deviation']))
                baseline_display = self._convert_to_display_units(peak['baseline'])
                
                self.unmatched_table.setItem(row, 4, QTableWidgetItem(f"{deviation_display:.4f}"))
                self.unmatched_table.setItem(row, 5, QTableWidgetItem(f"{baseline_display:.4f}"))
    
    def update_statistics(self):
        """Calculate and display summary statistics."""
        # For now, just show baseline statistics
        if not self.current_data_structure:
            self.total_pairs_label.setText("--")
            self.mean_deviation_label.setText("--")
            self.mean_time_between_label.setText("--")
            self.baseline_mean_label.setText("--")
            self.baseline_std_label.setText("--")
            self.baseline_slope_label.setText("--")
            return
        
        # Baseline statistics
        if self.current_data_structure.baseline_mean is not None:
            self.baseline_mean_label.setText(f"{self.current_data_structure.baseline_mean:.4f} Hz")
        else:
            self.baseline_mean_label.setText("--")
        
        if self.current_data_structure.baseline_std is not None:
            self.baseline_std_label.setText(f"{self.current_data_structure.baseline_std:.4f} Hz")
        else:
            self.baseline_std_label.setText("--")
        
        # Calculate baseline slope from baseline estimate if available
        if self.current_data_structure.dat_hp is not None:
            baseline_freqs = self.current_data_structure.dat_hp[:, 1]
            times = self.current_data_structure.timestamps
            if len(baseline_freqs) > 1 and len(times) > 1:
                # Fit linear trend to baseline
                try:
                    slope, intercept = np.polyfit(times, baseline_freqs, 1)
                    self.baseline_slope_label.setText(f"{slope:.4f} Hz/s")
                except:
                    self.baseline_slope_label.setText("--")
            else:
                self.baseline_slope_label.setText("--")
        else:
            self.baseline_slope_label.setText("--")
        
        # Peak statistics - show matched pairs if available, otherwise individual peaks
        if self.matched_peak_pairs:
            num_pairs = len(self.matched_peak_pairs)
            self.total_pairs_label.setText(str(num_pairs))
            
            mean_deviation = np.mean([p['mean_deviation'] for p in self.matched_peak_pairs])
            self.mean_deviation_label.setText(f"{mean_deviation:.4f} Hz")
            
            # Mean time between peaks in pairs
            mean_separation = np.mean([p['separation_time'] for p in self.matched_peak_pairs])
            self.mean_time_between_label.setText(f"{mean_separation:.6f} s")
        elif self.detected_individual_peaks:
            num_peaks = len(self.detected_individual_peaks)
            self.total_pairs_label.setText(f"0 (unmatched: {num_peaks})")
            
            mean_deviation = np.mean([abs(p['deviation']) for p in self.detected_individual_peaks])
            self.mean_deviation_label.setText(f"{mean_deviation:.4f} Hz")
            
            # Mean time between peaks (if more than one peak)
            if num_peaks > 1:
                peak_times = [p['time'] for p in self.detected_individual_peaks]
                peak_times_sorted = sorted(peak_times)
                time_diffs = [peak_times_sorted[i+1] - peak_times_sorted[i] for i in range(len(peak_times_sorted)-1)]
                mean_time_between = np.mean(time_diffs) if time_diffs else 0.0
                self.mean_time_between_label.setText(f"{mean_time_between:.6f} s")
            else:
                self.mean_time_between_label.setText("--")
        else:
            self.total_pairs_label.setText("0")
            self.mean_deviation_label.setText("--")
            self.mean_time_between_label.setText("--")


